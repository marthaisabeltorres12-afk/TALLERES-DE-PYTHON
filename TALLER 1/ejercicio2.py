#debemos solicitar el area principal
areaInicial = int(input("ingrese el area inicial de la cancha: "))
#calcular la ampiacion del 20%
areaAmpliada = areaInicial * 0.20
#calcular el area total
areaTotal = areaInicial + areaAmpliada

print(f"el area inicial de la cancha es: {areaInicial}")
print(f"el area ampliada de la cancha es: {areaAmpliada}")
print(f"el area total de la cancha es: {areaTotal}")